package BuilderPattern;

public class Emas {

    private Integer gram;

    private Double harga;


    public Emas(Integer gram, Double harga) {
        this.gram = gram;
        this.harga = harga;
    }

    public Integer getGram() {
        return gram;
    }


    public Double getHarga() {
        return harga;
    }

}
